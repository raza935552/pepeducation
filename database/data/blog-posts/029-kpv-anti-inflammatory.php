<p>KPV's primary mechanism targets NF-kB (nuclear factor kappa-light-chain-enhancer of activated B cells), often called the master switch of inflammation. NF-kB controls the expression of hundreds of pro-inflammatory genes. When it's overactivated, as happens in chronic inflammatory conditions, the result is persistent inflammation that damages healthy tissue.</p>

