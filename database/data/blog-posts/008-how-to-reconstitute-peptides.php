<ul>
<li><strong>Using sterile water instead of BAC water.</strong> Sterile water has no preservative. Multi-dose use creates infection risk.</li>
<li><strong>Spraying water directly onto the powder.</strong> This denatures the peptide. Always run water down the glass wall.</li>
<li><strong>Shaking the vial.</strong> Gentle rolling only. Shaking creates foam and damages the protein structure.</li>
