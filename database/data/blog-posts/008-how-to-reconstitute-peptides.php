<p>Why bacteriostatic water specifically? Plain sterile water has no preservative. Once you puncture the vial, bacteria can enter with each subsequent needle insertion. BAC water's benzyl alcohol inhibits bacterial growth, giving you a safe 28-day window. Since most peptide vials contain multiple doses, you'll be puncturing the stopper repeatedly. BAC water is non-negotiable.</p>

<h2>Step 1: Clean the Vial Stoppers</h2>

<p>Wipe the rubber stopper on both the peptide vial and the BAC water vial with an alcohol swab. Wipe firmly for at least 10-15 seconds per vial. Let the alcohol dry completely (about 30 seconds) before inserting a needle. This step prevents contamination from skin oils, dust, or bacteria on the stopper surface.</p>

<h2>Step 2: Withdraw Bacteriostatic Water</h2>

<p>Using a fresh insulin syringe, draw air equal to the amount of BAC water you plan to withdraw (this equalizes pressure in the vial). Insert the needle into the BAC water vial, inject the air, then invert the vial and slowly withdraw the desired amount of water.</p>

<p>How much water to use depends on your peptide amount and desired concentration. We'll cover the math in the dosing section below. A common starting point: for a 5mg peptide vial, use 2mL of BAC water. For a 10mg vial, use 2-3mL.</p>

<h2>Step 3: Add Water to the Peptide Vial</h2>

<p>This step requires care. Insert the syringe needle through the peptide vial's rubber stopper at an angle so the tip touches the inside glass wall of the vial (not the powder directly). Slowly depress the plunger, allowing the water to trickle down the glass wall and pool at the bottom.</p>

<p>Do not spray water directly onto the powder. Do not push the plunger quickly. Peptides are fragile proteins, and forceful mixing can damage their structure (denaturing). The water should flow gently down the side of the vial.</p>

<h2>Step 4: Mix Gently</h2>

<p>Once the water is in the vial, the powder needs to dissolve completely. Do not shake the vial. Shaking creates bubbles and foam that can denature the peptide.</p>

<p>Instead, gently roll the vial between your palms. Tilt it slightly back and forth. If the powder doesn't dissolve immediately, set the vial in the refrigerator for 15-20 minutes and come back to it. Some peptides dissolve quickly (30 seconds). Others take several minutes. The solution should be clear when fully dissolved. If it remains cloudy or has particles floating in it, something may be wrong with the product.</p>

