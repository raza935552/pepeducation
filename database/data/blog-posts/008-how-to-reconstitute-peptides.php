<h2>What You Need Before Starting</h2>

<p>Gather everything before you begin. Working with sterile materials means you don't want to stop mid-process to hunt for supplies.</p>

<ul>
<li><strong>Peptide vial</strong> (freeze-dried powder)</li>
<li><strong>Bacteriostatic water (BAC water)</strong> in a sealed vial. Not sterile water, not saline. BAC water contains 0.9% benzyl alcohol, which prevents bacterial growth during the 28-day usable window.</li>
