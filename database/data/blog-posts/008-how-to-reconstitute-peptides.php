<p>This is where most beginners get confused. The formula is simple once you understand it.</p>

<p><strong>Concentration = Peptide Amount / Water Volume</strong></p>

<p>Example: You have a 5mg (5,000 mcg) vial and add 2mL of BAC water.</p>

<p>5,000 mcg / 2 mL = 2,500 mcg per mL</p>

<p>An insulin syringe holds 1mL and has 100 unit markings. So each "unit" mark on the syringe = 25 mcg in this example.</p>

<p>If your dose is 250 mcg, you draw to the 10 unit mark (10 x 25 = 250 mcg).</p>

<p>Another common setup: 10mg vial + 2mL BAC water = 5,000 mcg per mL = 50 mcg per unit mark. A 500 mcg dose would be 10 units on the syringe.</p>

<p>Write your concentration on the vial with a marker so you don't have to recalculate every time. Some people use a simple label: "5mg/2mL = 25mcg per tick."</p>

<h2>Storage After Reconstitution</h2>

<p>Once reconstituted, peptides must be refrigerated at 2-8 degrees Celsius (36-46 degrees Fahrenheit). Standard refrigerator temperature is fine. Store the vial upright, away from light, ideally in a small box or bag in the back of the fridge where temperature is most stable.</p>

<p>Reconstituted peptides with BAC water are generally usable for up to 28 days. Some peptides degrade faster than others, but 28 days is the standard guideline. After that, discard remaining solution and reconstitute a fresh vial.</p>

<p>Unreconstituted (dry powder) peptides can be stored in the freezer for months or even years without significant degradation. Once you add water, the clock starts. Only reconstitute what you plan to use within the next month.</p>

